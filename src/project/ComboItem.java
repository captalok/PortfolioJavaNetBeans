public class ComboItem {
    private String name;
    private int value;

    public ComboItem(int value, String name) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public int getValue() {
        return value;
    }

    //@Override
    public String toString() {
        return name;
    }
}
