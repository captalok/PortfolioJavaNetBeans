    private Map<String, Integer> nameToIdMap = new HashMap<>();
    private void populateUserNameComboBox() {
        String query = "SELECT DocUserID, DocUserName FROM DocUserT";
        ResultSet rs = Select.getData(query);

        try {
            while (rs.next()) {
                String docUserName = rs.getString("DocUserName");
                jComboBox2.addItem(docUserName); // Add broker name to JComboBox
                // Assuming brokerId is retrieved from the ResultSet
                int docID = rs.getInt("DocUserID");
                // Add mapping between name and ID to the map
                nameToIdMap.put(docUserName, docID);
                
                if (docID == 4) {
                jComboBox2.setSelectedItem(docUserName); // Set the default value in the JComboBox
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            try {
                if (rs != null) {
                    rs.close();
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }
