    private void pickDocumentPath(){
        jButton6.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                // Check if the existing path exists
                String existingPath = jTextField13.getText();
                if (!existingPath.isEmpty()) {
                    File existingFile = new File(existingPath);
                    if (existingFile.exists()) {
                        // If the existing file exists, delete it
                        if (!existingFile.delete()) {
                            // Show error message if unable to delete the file
                            JOptionPane.showMessageDialog(null, "Unable to delete existing file", "Error", JOptionPane.ERROR_MESSAGE);
                            return;
                        }
                    }
                }
                
                // Create a file chooser dialog
                JFileChooser fileChooser = new JFileChooser();
                
                // Show the open file dialog
                int returnValue = fileChooser.showOpenDialog(null);
                
                // If a file is selected
                if (returnValue == JFileChooser.APPROVE_OPTION) {
                    File selectedFile = fileChooser.getSelectedFile();
                    
                    // Get the values from jTextField2 and jTextField3
                    String docID = jTextField11.getText();
                    String docName = jTextField12.getText();
                    
                    // Construct the destination path
                    String destination = "D:\\AccessDocs\\" + docID + "_" + docName + getFileExtension(selectedFile);
                    
                    try {
                        // Copy the selected file to the destination path
                        Files.copy(selectedFile.toPath(), Path.of(destination), StandardCopyOption.REPLACE_EXISTING);
                        
                        // Set the destination path in txtDocPath and jTextField1
                        //txtDocPath.setText(destination);
                        //int secondIndex = destination.indexOf("\\", destination.indexOf("\\") + 1);
                        //String newValue = destination.substring(secondIndex -10);
                        jTextField13.setText(destination);
                        
                    } catch (IOException ex) {
                        ex.printStackTrace();
                        JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
        });
        
        // Add action listener to btnOpen
        jButton2.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                // Get the file path from jTextField1
                String filePath = jTextField13.getText();
                
                // Check if the file path is not empty
                if (!filePath.isEmpty()) {
                    try {
                        // Open the file using the default associated application
                        Desktop.getDesktop().open(new File(filePath));
                    } catch (IOException ex) {
                        ex.printStackTrace();
                        JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "File path is empty", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        });
    }
    
    private String getFileExtension(File file) {
        String name = file.getName();
        int lastIndexOfDot = name.lastIndexOf(".");
        if (lastIndexOfDot != -1 && lastIndexOfDot != 0) {
            return name.substring(lastIndexOfDot);
        }
        return "";
    }
