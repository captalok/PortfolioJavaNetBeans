    public static DocumentInsert getInstance(int docID, String docUserName, String docName, String docPath, String docText) {
        if (instance == null) {
            instance = new DocumentInsert(docID, docUserName, docName, docPath, docText);
        } else {            
            // If an instance already exists, update its fields with new data
            instance.updateFields(docID, docUserName, docName, docPath, docText);
        }
        return instance;
        
    }
    
