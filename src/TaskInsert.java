    // Declare nameToIdMap as a class member
    private Map<String, Integer> nameToIdMap = new HashMap<>();
    
    private void populateTaskTypeComboBox() {
        String query = "SELECT TaskTypeID, TaskType FROM TaskTypeT";
        ResultSet rs = Select.getData(query);

        try {
            while (rs.next()) {
                String taskType = rs.getString("TaskType");
                jComboBox2.addItem(taskType); // Add broker name to JComboBox
                // Assuming brokerId is retrieved from the ResultSet
                int taskTypeID = rs.getInt("TaskTypeID");
                // Add mapping between name and ID to the map
                nameToIdMap.put(taskType, taskTypeID);
                
                if (taskTypeID == 1) {
                jComboBox2.setSelectedItem(taskType); // Set the default value in the JComboBox
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            try {
                if (rs != null) {
                    rs.close();
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }
    
