        String description = jTextField12.getText();      
        
        String importance = (String) jComboBox3.getSelectedItem();        
        int importanceID = nameToIdMap.get(importance);
        
        String dateDue = jTextField14.getText();
        String dateCompleted = jTextField15.getText();
        
        String incharge = (String) jComboBox4.getSelectedItem();        
        int inchargeID = nameToIdMap.get(incharge);
        
        String note = jTextField19.getText();
        
        int checkBoxValue;
        if (jCheckBox1.isSelected()) {
    
            checkBoxValue = 1;
        } else {
    
            checkBoxValue = 0;
        }
               
        
        try
        { 
            String query="UPDATE TaskT SET DateCreated = '"+dateCreated+"', TaskTypeID = '"+taskTypeID+"'," +
                        " Description = '"+description+"', ImportanceID = '"+importanceID+"', DateDue = '"+dateDue+"', DateCompleted = '"+dateCompleted+"'," +
                        " InchargeID = '"+inchargeID+"', Note = '"+note+"', IsActive = '"+checkBoxValue+"' WHERE TaskID = '"+taskID+"' ";
            
                InsertUpdateDelete.setData(query, "Data Updated Successfully");   
                
        observer.updateTask();
        jTextField12.requestFocusInWindow();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        
        
